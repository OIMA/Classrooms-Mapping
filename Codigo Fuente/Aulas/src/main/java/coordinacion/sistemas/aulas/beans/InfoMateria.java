package coordinacion.sistemas.aulas.beans;

/**
 *
 * @author OIMA
 */
public class InfoMateria {

    private String nombre, noGrupos, porcentaje, id, color;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getNoGrupos() {
        return noGrupos;
    }

    public void setNoGrupos(String noGrupos) {
        this.noGrupos = noGrupos;
    }

    public String getPorcentaje() {
        return porcentaje;
    }

    public void setPorcentaje(String porcentaje) {
        this.porcentaje = porcentaje;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
